/* global process */
import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = dirname(dirname(fileURLToPath(import.meta.url)));
const outputSql = join(root, 'supabase', 'seed', 'event_name_normalization.generated.sql');
const outputAudit = join(root, 'data', 'event_name_normalization.generated.json');
const eventSources = ['happity', 'better_start_for_life', 'eventbrite', 'fever'];

function readDotEnv(name) {
  try {
    return Object.fromEntries(readFileSync(join(root, name), 'utf8').replace(/^\uFEFF/, '')
      .split(/\r?\n/).filter((line) => line && !line.trim().startsWith('#') && line.includes('='))
      .map((line) => {
        const index = line.indexOf('=');
        return [line.slice(0, index).trim(), line.slice(index + 1).trim().replace(/^['"]|['"]$/g, '')];
      }));
  } catch {
    return {};
  }
}

const localEnv = readDotEnv('.env.local');
const supabaseUrl = process.env.VITE_SUPABASE_URL || localEnv.VITE_SUPABASE_URL;
const supabaseAnonKey = process.env.VITE_SUPABASE_ANON_KEY || localEnv.VITE_SUPABASE_ANON_KEY;

export function plainEventName(value) {
  return String(value || '')
    .normalize('NFKD')
    .replace(/[\u0300-\u036f]/g, '')
    .replaceAll('&', ' and ')
    .replace(/[^A-Za-z0-9]+/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

function sql(value) {
  return `$$${String(value).replaceAll('$$', '$ $')}$$`;
}

async function fetchEventRows() {
  if (!supabaseUrl || !supabaseAnonKey) throw new Error('Missing Supabase configuration.');
  const rows = [];
  for (let offset = 0; ; offset += 1000) {
    const response = await fetch(`${supabaseUrl}/rest/v1/activities?select=activity_id,activity_name,data_source&public_listing_status=eq.published&data_source=in.(${eventSources.join(',')})&order=activity_id.asc&limit=1000&offset=${offset}`, {
      headers: { apikey: supabaseAnonKey, Authorization: `Bearer ${supabaseAnonKey}` },
    });
    if (!response.ok) throw new Error(`Supabase returned ${response.status}`);
    const page = await response.json();
    rows.push(...page);
    if (page.length < 1000) break;
  }
  return rows;
}

function buildSql(updates) {
  if (!updates.length) return '-- All event names are already plain ASCII.\n';
  return `-- Generated by scripts/normalize-event-names.js\n-- Event titles are limited to letters, numbers and spaces.\n\nwith updates (activity_id, activity_name) as (\n  values\n    ${updates.map((item) => `(${sql(item.activity_id)}::uuid, ${sql(item.activity_name)}::text)`).join(',\n    ')}\n)\nupdate public.activities as activity\nset activity_name = updates.activity_name, updated_at = now()\nfrom updates\nwhere activity.activity_id = updates.activity_id;\n`;
}

async function main() {
  const rows = await fetchEventRows();
  const updates = rows
    .map((row) => ({ ...row, normalized_name: plainEventName(row.activity_name) }))
    .filter((row) => row.normalized_name && row.normalized_name !== row.activity_name)
    .map((row) => ({ activity_id: row.activity_id, activity_name: row.normalized_name, data_source: row.data_source }));
  mkdirSync(dirname(outputSql), { recursive: true });
  mkdirSync(dirname(outputAudit), { recursive: true });
  writeFileSync(outputSql, buildSql(updates));
  writeFileSync(outputAudit, JSON.stringify({ generated_at: new Date().toISOString(), sources: eventSources, total_rows: rows.length, updates }, null, 2) + '\n');
  console.log(`Normalised ${updates.length} of ${rows.length} event names.`);
}

main().catch((error) => {
  console.error(error.message);
  process.exit(1);
});
